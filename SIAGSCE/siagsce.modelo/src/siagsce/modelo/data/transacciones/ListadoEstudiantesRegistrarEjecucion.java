package siagsce.modelo.data.transacciones;

import java.util.Date;

public class ListadoEstudiantesRegistrarEjecucion {
	
	private String cedulaEstudiante, nombreEstudiante, apellidoEstudiante, horasEjecutadas;
	private Date fechaEjecucion;
	private Integer codigoActividadAsignada;

	public ListadoEstudiantesRegistrarEjecucion(String cedulaEstudiante,
			String nombreEstudiante, String apellidoEstudiante,
			String horasEjecutadas, Date fechaEjecucion,
			Integer codigoActividadAsignada) {
		super();
		this.cedulaEstudiante = cedulaEstudiante;
		this.nombreEstudiante = nombreEstudiante;
		this.apellidoEstudiante = apellidoEstudiante;
		this.horasEjecutadas = horasEjecutadas;
		this.fechaEjecucion = fechaEjecucion;
		this.codigoActividadAsignada = codigoActividadAsignada;
	}

	public String getCedulaEstudiante() {
		return cedulaEstudiante;
	}

	public void setCedulaEstudiante(String cedulaEstudiante) {
		this.cedulaEstudiante = cedulaEstudiante;
	}

	public String getNombreEstudiante() {
		return nombreEstudiante;
	}

	public void setNombreEstudiante(String nombreEstudiante) {
		this.nombreEstudiante = nombreEstudiante;
	}

	public String getApellidoEstudiante() {
		return apellidoEstudiante;
	}

	public void setApellidoEstudiante(String apellidoEstudiante) {
		this.apellidoEstudiante = apellidoEstudiante;
	}

	public String getHorasEjecutadas() {
		return horasEjecutadas;
	}

	public void setHorasEjecutadas(String horasEjecutadas) {
		this.horasEjecutadas = horasEjecutadas;
	}

	public Date getFechaEjecucion() {
		return fechaEjecucion;
	}

	public void setFechaEjecucion(Date fechaEjecucion) {
		this.fechaEjecucion = fechaEjecucion;
	}

	public Integer getCodigoActividadAsignada() {
		return codigoActividadAsignada;
	}

	public void setCodigoActividadAsignada(Integer codigoActividadAsignada) {
		this.codigoActividadAsignada = codigoActividadAsignada;
	}
	
	

}

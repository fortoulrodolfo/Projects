package siagsce.modelo.data.transacciones;

import java.util.Date;

public class ListadoEstudiantesActividadAsignada {
	
	private String cedula, nombre, apellido;
	private Date fechaEjecucion;
	private Integer horasEjecutadas, codigo_actividad_asignada;
	public String getCedula() {
		return cedula;
	}

	public void setCedula(String cedula) {
		this.cedula = cedula;
	}

	public String getNombre() {
		return nombre;
	}

	public void setNombre(String nombre) {
		this.nombre = nombre;
	}

	public String getApellido() {
		return apellido;
	}

	public void setApellido(String apellido) {
		this.apellido = apellido;
	}
	
	public Integer getHorasEjecutadas() {
		return horasEjecutadas;
	}

	public void setHorasEjecutadas(Integer horasEjecutadas) {
		this.horasEjecutadas = horasEjecutadas;
	}

	public Date getFechaEjecucion() {
		return fechaEjecucion;
	}

	public void setFechaEjecucion(Date fechaEjecucion) {
		this.fechaEjecucion = fechaEjecucion;
	}
	
	public Integer getCodigo_actividad_asignada() {
		return codigo_actividad_asignada;
	}

	public void setCodigo_actividad_asignada(Integer codigo_actividad_asignada) {
		this.codigo_actividad_asignada = codigo_actividad_asignada;
	}

	public ListadoEstudiantesActividadAsignada(String cedula, String nombre,
			String apellido, Integer codigo_actividad_asignada) {
		super();
		this.cedula = cedula;
		this.nombre = nombre;
		this.apellido = apellido;
		this.codigo_actividad_asignada = codigo_actividad_asignada;
	}

}

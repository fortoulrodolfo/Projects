package siagsce.modelo.data.transacciones;

import java.util.Date;

public class ListadoEstudiantesProyectoCulminado {
	
	private String cedula, nombre, apellido, nombre_actividad;
	private Integer codigo_actividad_asignada;
	
	public String getCedula() {
		return cedula;
	}

	public void setCedula(String cedula) {
		this.cedula = cedula;
	}

	public String getNombre() {
		return nombre;
	}

	public void setNombre(String nombre) {
		this.nombre = nombre;
	}

	public String getApellido() {
		return apellido;
	}

	public void setApellido(String apellido) {
		this.apellido = apellido;
	}
	
	
	public Integer getCodigo_actividad_asignada() {
		return codigo_actividad_asignada;
	}

	public void setCodigo_actividad_asignada(Integer codigo_actividad_asignada) {
		this.codigo_actividad_asignada = codigo_actividad_asignada;
	}

	public String getNombre_actividad() {
		return nombre_actividad;
	}

	public void setNombre_actividad(String nombre_actividad) {
		this.nombre_actividad = nombre_actividad;
	}

	public ListadoEstudiantesProyectoCulminado(String cedula, String nombre,
			String apellido, String nombre_actividad,
			Integer codigo_actividad_asignada) {
		super();
		this.cedula = cedula;
		this.nombre = nombre;
		this.apellido = apellido;
		this.nombre_actividad = nombre_actividad;
		this.codigo_actividad_asignada = codigo_actividad_asignada;
	}

	
}

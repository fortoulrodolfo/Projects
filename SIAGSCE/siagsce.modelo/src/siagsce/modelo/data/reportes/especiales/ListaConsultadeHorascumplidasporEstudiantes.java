package siagsce.modelo.data.reportes.especiales;

import java.math.BigInteger;




/** @Modelo ListadoConsultaHorascumplidasporEstudiantes - Reporte Especial
 * Modelo usado en la generación del reporte Consulta de horas cumplidas por estudiantes
 * asignadas a estudiantes en el SCE
 * @author Iterator
 */

public class ListaConsultadeHorascumplidasporEstudiantes {
	
	

/**
 * Declaración de variables.
 */
	private String cedula;
	private String nombre;
	private String apellido;
	private String direccionPrograma;
	private BigInteger horasCumplidas;
	
	
	
	/**
	 * Declaración del constructor
	 */
	public ListaConsultadeHorascumplidasporEstudiantes(String cedula,
			String nombre, String apellido,String DireccionPrograma,BigInteger horasCumplidas) {
		super();
		this.cedula = cedula;
		this.nombre = nombre;
		this.apellido = apellido;
		this.horasCumplidas = horasCumplidas;
		this.direccionPrograma=DireccionPrograma;
	}
	
	/**
	 * Declaración de set y get
	 */
	public String getDireccionPrograma() {
		return direccionPrograma;
	}

	public void setDireccionPrograma(String direccionPrograma) {
		this.direccionPrograma = direccionPrograma;
	}
	public String getCedula() {
		return cedula;
	}
	public void setCedula(String cedula) {
		this.cedula = cedula;
	}
	public String getNombre() {
		return nombre;
	}
	public void setNombre(String nombre) {
		this.nombre = nombre;
	}
	public String getApellido() {
		return apellido;
	}
	public void setApellido(String apellido) {
		this.apellido = apellido;
	}
	
	public BigInteger getHorasCumplidas() {
		return horasCumplidas;
	}

	public void setHorasCumplidas(BigInteger horasCumplidas) {
		this.horasCumplidas = horasCumplidas;
	}
	
}
	
	
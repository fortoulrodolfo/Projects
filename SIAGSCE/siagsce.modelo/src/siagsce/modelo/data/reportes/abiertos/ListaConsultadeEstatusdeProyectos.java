package siagsce.modelo.data.reportes.abiertos;

/** @Modelo ListadoConsultadeEstatusProyectos - Reporte Abierto
 * Modelo usado en la generación del reporte Consulta Estatus de Proyectos 
 * asignadas a estudiantes en el SCE
 * @author Iterator
 */


public class ListaConsultadeEstatusdeProyectos {
	
	
	private String codigo;
	private String nombre;
	private String descripcion;
	private String nombreDireccion;
	
	
	/**
	 * Declaración del constructor
	 */
	public ListaConsultadeEstatusdeProyectos(String codigo, String nombre,
			String descripcion, String nombreDireccion) {
		super();
		this.codigo = codigo;
		this.nombre = nombre;
		this.descripcion = descripcion;
		this.nombreDireccion = nombreDireccion;
	}
	
	
	/**
	 * Declaración de set y get
	 */
	public String getCodigo() {
		return codigo;
	}
	public void setCodigo(String codigo) {
		this.codigo = codigo;
	}
	public String getNombre() {
		return nombre;
	}
	public void setNombre(String nombre) {
		this.nombre = nombre;
	}
	public String getDescripcion() {
		return descripcion;
	}
	public void setDescripcion(String descripcion) {
		this.descripcion = descripcion;
	}
	public String getNombreDireccion() {
		return nombreDireccion;
	}
	public void setNombreDireccion(String nombreDireccion) {
		this.nombreDireccion = nombreDireccion;
	}
	
		
}
